package cn.granitech.variantorm.exception;


public class MetadataSpacesException extends RuntimeException {
    public MetadataSpacesException(Throwable cause) {
        super(cause);
    }

    public MetadataSpacesException() {}

    public MetadataSpacesException(String s) {
        super(s);
    }

    public MetadataSpacesException(String s, Throwable cause) {
        super(s, cause);
    }
}
